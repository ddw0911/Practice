package practice.part2;

public class part2_2 {
    public static void main(String[] args) {
        // double 타입 상수 PI를 만들어보세요.
        // 상수는 final 키워드를 사용해 선언하세요.
        //  상수는 대문자로만 씁니다.
        int radius = 5;
        final double PI = 3.14159;
        double result = radius * 2 * PI;
        System.out.println(result);
    }
}